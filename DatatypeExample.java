class DatatypeExample 
{
	public static void main(String[] args) 
	{
		
		int batchNo=9101;
		double salary=35000.50;
		boolean status=false;
		char grade='B';
		float height=5.9f;
		long phoneNo=3456789012L;
		byte marks=70;
		short age=34;
		//printing statement
		System.out.println("Integer Value: "+batchNo);
		System.out.println("Double Value: "+salary);
		System.out.println("Boolean Value: "+status);
		System.out.println("Character Value: "+grade);
		System.out.println("Float Value: "+height);
		System.out.println("Long Value: "+phoneNo);
		System.out.println("Byte Value: "+marks);
		System.out.println("Short Value: "+age);
		
	}
}
